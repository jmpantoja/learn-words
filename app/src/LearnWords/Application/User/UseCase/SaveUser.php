declare(strict_types=1);

namespace LearnWords\Application\User\UseCase;


use LearnWords\Domain\User\User;

final class SaveUser
{
    /**
     * @var User
     */
    private User $user;

    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * @return User
     */
    public function getUser(): User
    {
        return $this->user;
    }
}
